    /* Case 18: Add c-a h-f g-d e-b for tour AdCB */
    if (M[c][a] + M[h][f] + M[g][d] + M[e][b] < r) {
       I[0]=c; I[1]=a; I[2]=h; I[3]=f;
       I[4]=g; I[5]=d; I[6]=e; I[7]=b;
       return;
    }

    /* Case 19: Add c-a h-f g-e d-b for tour AdCb */
    if (M[c][a] + M[h][f] + M[g][e] + M[d][b] < r) {
       I[0]=c; I[1]=a; I[2]=h; I[3]=f;
       I[4]=g; I[5]=e; I[6]=d; I[7]=b;
       return;
    }

    /* Case 20: Add c-h a-d e-g f-b for tour ADBc */
    if (M[c][h] + M[a][d] + M[e][g] + M[f][b] < r) {
       I[0]=c; I[1]=h; I[2]=a; I[3]=d;
       I[4]=e; I[5]=g; I[6]=f; I[7]=b;
       return;
    }

    /* Case 21: Add c-h a-e d-f g-b for tour ADbC */
    if (M[c][h] + M[a][e] + M[d][f] + M[g][b] < r) {
       I[0]=c; I[1]=h; I[2]=a; I[3]=e;
       I[4]=d; I[5]=f; I[6]=g; I[7]=b;
       return;
    }

    /* Case 22: Add c-h a-e d-g f-b for tour ADbc */
    if (M[c][h] + M[a][e] + M[d][g] + M[f][b] < r) {
       I[0]=c; I[1]=h; I[2]=a; I[3]=e;
       I[4]=d; I[5]=g; I[6]=f; I[7]=b;
       return;
    }

    /* Case 23: Add c-a h-d e-g f-b for tour AdBc */
    if (M[c][a] + M[h][d] + M[e][g] + M[f][b] < r) {
       I[0]=c; I[1]=a; I[2]=h; I[3]=d;
       I[4]=e; I[5]=g; I[6]=f; I[7]=b;
       return;
    }

    /* Case 24: Add c-a h-e d-f g-b for tour AdbC */
    if (M[c][a] + M[h][e] + M[d][f] + M[g][b] < r) {
       I[0]=c; I[1]=a; I[2]=h; I[3]=e;
       I[4]=d; I[5]=f; I[6]=g; I[7]=b;
       return;
    }

    /* Case 25: Add c-a h-e d-g f-b for tour Adbc */
    if (M[c][a] + M[h][e] + M[d][g] + M[f][b] < r) {
       I[0]=c; I[1]=a; I[2]=h; I[3]=e;
       I[4]=d; I[5]=g; I[6]=f; I[7]=b;
       return;
    }

    *good = 0;
