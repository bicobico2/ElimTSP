/* The following functions examine ways to reconnect a tour after  */
/* deleting 3, 4, or 5 edges. The code was generated by genswap.c  */

void CCelim_compare_three_swap (int a, int b, int c, int d, int e, int f,
        int **M, int *good, int *I)
{
    int r;

    *good = 1;

    /* Delete a-b c-d e-f */

    r = M[a][b] + M[c][d] + M[e][f];

    /* Case 1: Add c-e d-a f-b for tour Abc */
    if (M[c][e] + M[d][a] + M[f][b] < r) {
        I[0]=c; I[1]=e; I[2]=d; I[3]=a; I[4]=f; I[5]=b;
        return;
    }

    /* Case 2: Add c-f a-d e-b for tour ACB */
    if (M[c][f] + M[a][d] + M[e][b] < r) {
        I[0]=c; I[1]=f; I[2]=a; I[3]=d; I[4]=e; I[5]=b;
        return;
    }

    /* Case 3: Add c-f a-e d-b for tour ACb */
    if (M[c][f] + M[a][e] + M[d][b] < r) {
        I[0]=c; I[1]=f; I[2]=a; I[3]=e; I[4]=d; I[5]=b;
        return;
    }

    /* Case 4: Add c-a f-d e-b for tour AcB */
    if (M[c][a] + M[f][d] + M[e][b] < r) {
        I[0]=c; I[1]=a; I[2]=f; I[3]=d; I[4]=e; I[5]=b;
        return;
    }

    *good = 0;
}

